// Command gen reads routing.yaml and generates router_gen.go.
// It is invoked via go generate from the parent binding package.
package main

import (
	"cmp"
	"fmt"
	"os"
	"path/filepath"
	"slices"
	"text/template"

	"gopkg.in/yaml.v3"
)

// routingFile is the top-level structure of routing.yaml.
type routingFile struct {
	Routing        map[string]string            `yaml:"routing"`
	SkillOverrides map[string]map[string]string `yaml:"skill_overrides"`
	BugStatuses    []string                     `yaml:"bug_statuses"`
}

// templateData is the data passed to the Go template.
type templateData struct {
	Timestamp        string
	ValidStages      map[string]bool
	WorkableStatuses []string
	RoutingTable     map[string]string
	BugCases         []bugCase
	BugStatuses      []string
	ValidTiers       map[string]bool
}

type bugCase struct {
	Status string
	Key    string
}

const genTemplate = `// Code generated by internal/binding/gen from routing.yaml; DO NOT EDIT.

package binding

import "fmt"

// ─── Generated routing constructs ───────────────────────────────────────────

// validStages is the set of all valid stage binding keys.
// It includes all destination keys from the routing table.
var validStages = map[string]bool{
{{- range $stage, $_ := .ValidStages }}
	"{{ $stage }}": true,
{{- end }}
}

// WorkableStatuses lists all lifecycle statuses that are valid pipeline inputs.
// It includes both feature and bug statuses.
var WorkableStatuses = []string{
{{- range $status := .WorkableStatuses }}
	"{{ $status }}",
{{- end }}
}

// routingTable maps lifecycle statuses to their binding keys.
var routingTable = RoutingTable{
{{- range $status, $key := .RoutingTable }}
	"{{ $status }}": "{{ $key }}",
{{- end }}
}

// BugStatusToBindingKey maps a bug lifecycle status to its corresponding
// stage binding key. Only workable bug statuses (in-progress, needs-review)
// return a binding key; all other bug statuses return an error.
func BugStatusToBindingKey(status string) (string, error) {
	switch status {
{{- range $c := .BugCases }}
	case "{{ $c.Status }}":
		return "{{ $c.Key }}", nil
{{- end }}
	default:
		return "", fmt.Errorf("bug status %q is out-of-pipeline", status)
	}
}

// BugStatuses lists all known bug lifecycle statuses (both workable and non-workable).
var BugStatuses = []string{
{{- range $status := .BugStatuses }}
	"{{ $status }}",
{{- end }}
}

// ValidTiers maps valid tier names.
var ValidTiers = map[string]bool{
{{- range $tier, $_ := .ValidTiers }}
	"{{ $tier }}": true,
{{- end }}
}

// IsValidTier checks if a tier name is valid.
func IsValidTier(tier string) bool {
	return ValidTiers[tier]
}

// bugStatusToBindingKey wraps BugStatusToBindingKey for internal use.
func bugStatusToBindingKey(status string) (string, error) {
	return BugStatusToBindingKey(status)
}
`

func main() {
	// Determine paths relative to the generator binary location.
	// When run via go run ./gen routing.yaml router_gen.go, the working
	// directory is internal/binding/.
	cwd, err := os.Getwd()
	if err != nil {
		fmt.Fprintf(os.Stderr, "gen: cannot get working directory: %v\n", err)
		os.Exit(1)
	}

	// Check for explicit arguments (go generate passes them).
	var routingPath, outputPath string
	if len(os.Args) >= 3 {
		routingPath = filepath.Join(cwd, os.Args[1])
		outputPath = filepath.Join(cwd, os.Args[2])
	} else {
		// Fallback: use default paths.
		routingPath = filepath.Join(cwd, "routing.yaml")
		outputPath = filepath.Join(cwd, "router_gen.go")
	}

	data, err := os.ReadFile(routingPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "gen: cannot read %s: %v\n", routingPath, err)
		os.Exit(1)
	}

	var rf routingFile
	if err := yaml.Unmarshal(data, &rf); err != nil {
		fmt.Fprintf(os.Stderr, "gen: cannot parse %s: %v\n", routingPath, err)
		os.Exit(1)
	}

	// Build validStages: all binding key values (unique).
	validStages := make(map[string]bool)
	for _, key := range rf.Routing {
		validStages[key] = true
	}

	// Build WorkableStatuses: all status keys, sorted for determinism.
	workableStatuses := make([]string, 0, len(rf.Routing))
	for status := range rf.Routing {
		workableStatuses = append(workableStatuses, status)
	}
	slices.Sort(workableStatuses)

	// Build bug cases: only statuses that map to a different key (i.e., bug statuses).
	var bugCases []bugCase
	for status, key := range rf.Routing {
		if status != key {
			bugCases = append(bugCases, bugCase{Status: status, Key: key})
		}
	}
	// Sort for determinism.
	slices.SortFunc(bugCases, func(a, b bugCase) int {
		return cmp.Compare(a.Status, b.Status)
	})

	// Build bug statuses list (sorted for determinism).
	bugStatuses := make([]string, len(rf.BugStatuses))
	copy(bugStatuses, rf.BugStatuses)
	slices.Sort(bugStatuses)

	// Valid tiers (architectural constants).
	validTiers := map[string]bool{
		"feature":   true,
		"bug_fix":   true,
		"retro_fix": true,
		"critical":  true,
	}

	td := templateData{
		ValidStages:      validStages,
		WorkableStatuses: workableStatuses,
		RoutingTable:     rf.Routing,
		BugCases:         bugCases,
		BugStatuses:      bugStatuses,
		ValidTiers:       validTiers,
	}

	tmpl, err := template.New("router_gen").Parse(genTemplate)
	if err != nil {
		fmt.Fprintf(os.Stderr, "gen: template parse error: %v\n", err)
		os.Exit(1)
	}

	f, err := os.Create(outputPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "gen: cannot create %s: %v\n", outputPath, err)
		os.Exit(1)
	}
	defer f.Close()

	if err := tmpl.Execute(f, td); err != nil {
		fmt.Fprintf(os.Stderr, "gen: template execute error: %v\n", err)
		os.Exit(1)
	}

	fmt.Printf("gen: wrote %s (%d stages, %d statuses, %d bug cases)\n",
		outputPath, len(validStages), len(workableStatuses), len(bugCases))
}
